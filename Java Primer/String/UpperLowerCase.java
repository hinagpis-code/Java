public class UpperLowerCase{
  
 public static void main(String[] args){
  
   //toUpperCase() all string will be upper case
   String name = "hinagpis code";
   System.out.println("Name: " + name.toUpperCase()); //Output: Name: HINAGPIS CODE
   
   
   //toLowerCase() all string will be lower case
   String address = "123 FAKE STREET";
   System.out.println(address.toLowerCase()); //Output: 123 fake street
 }
  
}
