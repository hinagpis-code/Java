public class DataChar{
  
 public static void main(String[] args) {
   
   /**
   Char short for Character is a single-character text.
   - use single-quote if you are using character data type
   */
   
   char first_name = 'H';
   char last_name = 'C';
   
   System.out.print(first_name); //output: H
   System.out.print(last_name); //output: C
   
 }
  
}
