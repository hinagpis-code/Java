public class ArithmethicOperator{
  
 public static void main(String [] args){
  
   /* 
  Arithmetic operators are used to
  perform common mathematical operations.
  */
   
   int x = 10, y = 11;
   
   //Addition Operator (+)  = adds together the two value.
   System.out.println(x+y);
   
   //Subtraction Operator (+)  = Subtract one from another.
   System.out.println(x-y);
   
   //Multiplication Operator (*) = Multiplies the two values.
   System.out.println(x*y);
   
   //Division Operator (/) = Divides one from another.
    System.out.println(x/y);
   
   //Modulus Operator (%) = Return the remainder of division
    System.out.println(x%y);
   
   //Increment Operator (++) = Increase the value of variable by 1.
    System.out.println(x++);
    System.out.println(y++);
   
   //Decrement Operator (--) = Decrease the value of variable by 1.
     System.out.println(x--);
    System.out.println(y--);
   
 }
  
}
